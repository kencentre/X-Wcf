        /// <summary>
        /// 增加一条数据
        /// </summary>
        public bool Add(E_Model.Property model)
        {
            StringBuilder strSql = new StringBuilder();
            strSql.Append("insert into CMS_PROPERTY(");
            strSql.Append("ID,PROPERTYNAME,PROPERTYTYPE,DESCRIPTION,CREATEDATE,CREATEUSER)");

            strSql.Append(" values (");
            strSql.Append("@ID,@PROPERTYNAME,@PROPERTYTYPE,@DESCRIPTION,@CREATEDATE,@CREATEUSER)");
            Database db = DatabaseFactory.CreateDatabase();
            DbCommand dbCommand = db.GetSqlStringCommand(strSql.ToString());
            db.AddInParameter(dbCommand, "ID", DbType.AnsiString, model.ID);
            db.AddInParameter(dbCommand, "PROPERTYNAME", DbType.AnsiString, model.PROPERTYNAME);
            db.AddInParameter(dbCommand, "PROPERTYTYPE", DbType.AnsiString, model.PROPERTYTYPE);
            db.AddInParameter(dbCommand, "DESCRIPTION", DbType.AnsiString, model.DESCRIPTION);
            db.AddInParameter(dbCommand, "CREATEDATE", DbType.DateTime, model.CREATEDATE);
            db.AddInParameter(dbCommand, "CREATEUSER", DbType.AnsiString, model.CREATEUSER);
            int rows = db.ExecuteNonQuery(dbCommand);

            if (rows > 0)
            {
                return true;
            }
            else
            {
                return false;
            }
        }
        /// <summary>
        /// 更新一条数据
